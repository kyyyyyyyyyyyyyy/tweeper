     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $this->validate($request, [
            'image' => 'required|mimes:jpeg,jpg,png'
        ],[
            'image.required' => 'foto wajib di isi',
            'image.mimes' => 'format ini tidak di dukung',
        ]);

        $image = $request->file('image');
        $extensionImage = $image->extension();
        $nameImage = date('ymdhis') . "." . $extensionImage;
        $image -> move(public_path('image'), $nameImage);

        $value = User::where('id', $id)->first();

        $value->update([
            'image' => $nameImage
        ]);

        return redirect()->route('tweets.index');
    }

